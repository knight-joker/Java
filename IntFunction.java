package day10;

public class IntFunction {

	public static void main(String[] args) {
		// TODO 自动生成的方法存根
		System.out.println("使用ceil（）方法取整" + Math.ceil(5.3));//返回一个大于等于参数的整数
		System.out.println("使用floor()方法取整" + Math.floor(2.5));//返回一个小于等于参数的整数
		System.out.println("使用rint（）方法取整" + Math.rint(2.7));//返回一个与参数接近的整数
		System.out.println("使用round（）方法取整" + Math.round(3.4f));//将参数加0.5后返回最接近的整数
		System.out.println("使用round（）方法取整" + Math.round(2.5));//将参数加0.5后返回最接近的整数,并强制转化为长整型

	}

}
